<template><div><h2 id="概念" tabindex="-1"><a class="header-anchor" href="#概念" aria-hidden="true">#</a> 概念</h2>
<p>电子取证是指利用计算机软硬件技术，以符合法律规范的方式对计算机入侵、破坏、欺诈、攻击等犯罪行为进行证据获取、保存、分析和出示的过程。从技术方面看，计算机犯罪取证是一个对受侵计算机系统进行扫描和破解，对入侵事件进行重建的过程。具体而言，是指把计算机看作犯罪现场，运用先进的辨析技术，对计算机犯罪行为进行解剖，搜寻罪犯及其犯罪证据</p>
<h2 id="特点" tabindex="-1"><a class="header-anchor" href="#特点" aria-hidden="true">#</a> 特点</h2>
<ol>
<li>计算机数据无时无刻不在改变。</li>
<li>计算机数据不是肉眼直接可见的，必须借助适当的工具。</li>
<li>搜集计算机数据的过程，可能会对原始数据造成严重的修改。</li>
<li>电子证据问题是由于技术发展引起的，因为计算机和电信技术的发展非常迅猛，所以取证步骤和程序也必须不断调整以适应技术的进步。</li>
</ol>
<h2 id="基本流程" tabindex="-1"><a class="header-anchor" href="#基本流程" aria-hidden="true">#</a> 基本流程</h2>
<p>总体上可分为两个阶段 <strong>现场勘查阶段</strong> 和 <strong>证据分析阶段</strong></p>
<h3 id="现场勘查阶段" tabindex="-1"><a class="header-anchor" href="#现场勘查阶段" aria-hidden="true">#</a> 现场勘查阶段</h3>
<p>主要任务是获取可能的物理证据，如嫌疑人的计算机、移动硬盘、U盘、手机、数码相机、数字存储卡等各种可能包含证据的介质。</p>
<h3 id="证据分析阶段" tabindex="-1"><a class="header-anchor" href="#证据分析阶段" aria-hidden="true">#</a> 证据分析阶段</h3>
<p>对获取的存储设备进行深入检查，发现可能的电子证据并生成报告，以便提交给法庭作为诉讼证据。</p>
<h3 id="基本具体流程" tabindex="-1"><a class="header-anchor" href="#基本具体流程" aria-hidden="true">#</a> 基本具体流程</h3>
<ol>
<li><strong>取证准备</strong><br>
取证人员在前往现场进行勘查取证之前,必须做好充分的准备工作。包括了解案件的基本情况、现场所在的位置、现场可能有哪些人、要取证的对象可能包括哪些等。根据所了解的案情，准备前往现场的勘查人员和勘查设备。</li>
<li><strong>证据识别</strong><br>
识别现场可能包含证据的设备，如嫌疑人的计算机、打印机、存储介质（软盘、光盘、移动硬盘、U盘、CF卡）、其它电子设备（如PDA、手机、数码相机）。需要注意的是，现在存储介质的形状样式越来越多样化,如U盘，可以做成各种样式,应该注意识别这些被“隐藏”起来的证据。</li>
<li><strong>证据收集</strong><br>
收集现场发现的证物并做好记录。</li>
<li><strong>证据固定及获取</strong><br>
证据固定的目的，是要保护原始证据不被破坏。如当现场的计算机处于开机状态时，内存信息、屏幕信息、进程信息等关机后就会丢失的易丢失证据，需要将其提取出来进行保存，然后再关机。对于硬盘数据等非易失性数据，也须用硬盘复制机复制下来以便后续分析，从而避免硬盘硬件出现故障或硬盘中的数据被篡改。</li>
<li><strong>证据保存</strong><br>
证据保存是将获取的物证进行封装,以便存储和运输。电子证物的封装除了要考虑通常的防潮防震以外，部分证物还应注意防静电或进行信号屏蔽,如开机状态的手机,应立即放入信号屏蔽盒中，以避免新短信或电话呼叫导致覆盖手机内存或SIM卡中的原有信息。</li>
<li><strong>证据分析</strong><br>
证据分析过程是借助取证分析软件对获取的电子证据进行深入分析，挖掘出潜在的犯罪证据和线索。功能比较综合的取证软件如Encase、FTK、取证大师、火眼等。</li>
<li><strong>生成报告</strong><br>
将取证大师等软件结果生成符合规定的报告形式，取证软件本身一般都提供报告的制作和导出功能。</li>
</ol>
